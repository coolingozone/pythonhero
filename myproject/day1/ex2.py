# 1. Create a variable called `num1` and assign any integer value to it.
num1 = 20

# 2. Create a variable called `num2` and assign another integer value to it.
num2 = 10

# 3. Calculate the sum of `num1` and `num2` and store it in a variable called `sum_result`.
sum_result = num1 + num2

# 4. Calculate the difference between `num1` and `num2` and store it in a variable called `difference_result`.
difference_result = num1 - num2

# 5. Calculate the product of `num1` and `num2` and store it in a variable called `product_result`.
product_result = num1 * num2

# 6. Calculate the quotient (division) of `num1` by `num2` and store it in a variable called `quotient_result`.
quotient_result = num1 / num2

# 7. Calculate the remainder of `num1` divided by `num2` and store it in a variable called `remainder_result`.
remainder_result = num1 % num2

# 8. Print the results of all these operations.
print("Sum:", sum_result)
print("Difference:", difference_result)
print("Product:", product_result)
print("Quotient:", quotient_result)
print("Remainder:", remainder_result)
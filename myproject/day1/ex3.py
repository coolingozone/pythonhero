# 1. Create a variable called `string1` and assign a string value to it.
string1 = "Hello"

# 2. Create another variable called `string2` and assign a different string value to it.
string2 = "World"

# 3. Concatenate `string1` and `string2` and store the result in a variable called `concatenated_string`.
concatenated_string = string1 + " " + string2

# 4. Print the `concatenated_string`.
print("Concatenated String:", concatenated_string)

# 5. Find the length of `concatenated_string` and store it in a variable called `string_length`.
string_length = len(concatenated_string)

# 6. Print the `string_length`.
print("String Length:", string_length)

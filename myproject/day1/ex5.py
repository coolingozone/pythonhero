# 1. Create a variable called `number_string` and assign a numerical string (e.g., "42") to it.
number_string = "42"

# 2. Convert `number_string` to an integer and store it in a variable called `number_integer`.
number_integer = int(number_string)

# 3. Convert `number_string` to a float and store it in a variable called `number_float`.
number_float = float(number_string)

# 4. Print the types and values of `number_string`, `number_integer`, and `number_float`.
print("Type of number_string:", type(number_string), "Value:", number_string)
print("Type of number_integer:", type(number_integer), "Value:", number_integer)
print("Type of number_float:", type(number_float), "Value:", number_float)
